#include <iostream>
using namespace std;
int main()
{
    int a, b;
    cout << "Enter the first number : ";
    cin >> a;
    cout << "Enter the second number: ";
    cin >> b;
    if (a > b)
    {
        cout << " The first number is greater";
    }
    if (a == b)
    {
        cout << "Both  numbers are equal";
    }
    else
    {
        cout << "Second number is greater";
    }
}

